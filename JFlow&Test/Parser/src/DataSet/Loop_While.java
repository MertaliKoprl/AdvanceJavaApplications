package DataSet;

public class Loop_While {

    public static void main(String[] args) {

        int a = 3;

        while(a==3){//////////////WHILE
            a=2;
        }

        //////////////////////////Do-WHILE

        do{
            a=4;
        }
        while(a==2);

        //////////////////////////NESTED-WHILE
        while(a==4){

            while(a==4){

                a=3;

            }

        }

        /////////////////////////////

    }
}
